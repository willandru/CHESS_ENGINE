#include "Table3D.h"

Table3D::Table3D()
{
}



//====================================================
// INITIALIZATION
//====================================================

bool Table3D::load()
{

    //------------------------------------------------
    // LOAD MODEL
    //------------------------------------------------

    if(
        !loadModel(
            "Assets/Environment/basic_room_table.gltf"
        )
    )
    {
        return false;
    }



    //------------------------------------------------
    // DEFAULT TRANSFORM
    //------------------------------------------------

    setPosition({
        0.0f,
        0.0f,
        0.0f
    });

    setRotation({
        0.0f,
        0.0f,
        0.0f
    });

    setScale({
        1.0f,
        1.0f,
        1.0f
    });



    //------------------------------------------------
    // DEFAULT APPEARANCE
    //------------------------------------------------

    setColor({
        1.0f,
        1.0f,
        1.0f
    });

    return true;
}