#include "Chair3D.h"


Chair3D::Chair3D()
{
}



//====================================================
// INITIALIZATION
//====================================================

bool Chair3D::load()
{

    //------------------------------------------------
    // LOAD GLB MODEL
    //------------------------------------------------

    if(
        !loadGLB(
            "Assets/Environment/WoodDisk.glb"
        )
    )
    {
        return false;
    }



    //------------------------------------------------
    // TRANSFORM
    //------------------------------------------------

    setPosition({
        0.0f,
        0.0f,
        0.0f
    });


    setRotation({
        0.0f,
        0.0f,
        0.0f
    });


    setScale({
        1.0f,
        1.0f,
        1.0f
    });



    //------------------------------------------------
    // MATERIAL
    //------------------------------------------------

    setColor({
        1.0f,
        1.0f,
        1.0f
    });



    return true;
}